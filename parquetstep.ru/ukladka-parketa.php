    <h2>Подготовка паркета к укладке</h2>
<p>Для того чтобы положить паркет качественно и на продолжительный период перед началом работ необходимо соблюдать следующие правила:</p>
<ul>
<li>  провести тщательный визуальный осмотр доски и убедиться в отсутствии дефектов. При наличии отбраковки своевременно позаботиться о замене на качественный материал;    </li>
<li>привести температурный режим (от 18 до 23 °С), а также влажность  в помещении (от 40 до 60 %) и самих планок (в среднем 8,5%) к требуемым нормативам. Для акклиматизации древесины ее в распакованном виде выдерживают на месте предстоящих работ с оптимальными влажностно-температурными условиями до тех пор, пока не будет достигнут необходимый показатель. Этот процесс может занимать до 14 дней. Несоблюдение этого пункта может повлечь впоследствии к деформации готового напольного покрытия. </li>
</ul>
<h2>Устройство основания под монтаж паркетного пола</h2>
<p>Неровности основания пола приводят к скрипу паркета. Поэтому на первом этапе необходимо определить качество (наличие трещин, раковин, сколов, пустот, рельефностей) существующей стяжки и выявить перепады (более 2 мм на двухметровом отрезке) рабочей поверхности. В случае обнаружения любых дефектов произвести выравнивание шпаклевочной или цементно-песчаной смесью. Выступающие неровности обработать шлифовочной машиной.</p>
<p>Если работа начинается с нуля, то необходимо выполнить укладку стяжки толщиной минимум 4 см с двухмесячным сроком ожидания до полного высыхания. Монтаж штучного паркета производят только после того, как влажность основания составит не более 3%. Нарушение технологических стандартов в этом случае приводит к порче паркета, необходимости реконструкции и дополнительным затратам на приобретение расходных материалов, увеличивающих стоимость укладки паркета.</p>
<p>Существует более быстрый способ выравнивания поверхности основания с помощью влагостойкой фанеры. Крепить ее можно клеевым, бесклеевым или смешанным способом. В этом случае необходимо выполнение следующих правил. Во-первых, между листами требуется оставлять зазор 5 мм. Во-вторых, проверить на наличие пустот и, в случае необходимости, устранить их.</p>
<p>Еще один способ выравнивания основания заключается в том, что без дополнительных работ по стяжке, устанавливают лаги, которые крепятся к основанию. Полученные «соты» используют для заполнения шумоизолирующими материалами или утеплителями. Непосредственно к лагам крепят влагостойкую фанеру, толщиной не менее 18 мм. Кроме ровной поверхности основания такой вариант предоставляет возможность регулирования уровня или наклона пола.</p>
<p>Для гидроизоляции поверхности основания ее обрабатывают реактивной грунтовкой или эпоксидной смолой. Далее, с этой же целью, укладывают пленку, ее толщина должна быть не менее 200 мкм. Следующий слой, обеспечивающий теплоизоляцию, как правило, выполняют из вспененного полиэтилена или пенополистирола.</p>
