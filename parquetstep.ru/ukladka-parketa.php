<h3><a name="elochkoi"></a>Ёлочкой</h3>
<p><a href="http://test.parkett-service.ru/wp-content/uploads/2016/02/parket-elka.jpg" rel="attachment wp-att-5730"><img style="float: left; margin-right: 30px; margin-bottom: 20px;" src="http://test.parkett-service.ru/wp-content/uploads/2016/02/parket-elka-300x300.jpg" alt="parket-elka" width="300" height="300" class="alignleft size-medium wp-image-5730" srcset="https://parkettservice.ru/wp-content/uploads/2016/02/parket-elka-150x150.jpg 150w, https://parkettservice.ru/wp-content/uploads/2016/02/parket-elka-300x300.jpg 300w, https://parkettservice.ru/wp-content/uploads/2016/02/parket-elka.jpg 800w" sizes="(max-width: 300px) 100vw, 300px" /></a>
<p>Этот способ укладки визуально красив и практичен в силу равномерности распределения давления на планки, что в результате сводит к минимуму риск образования щелей, неровностей пола. Укладка ёлочкой выполняется с фризом или без него.</p>
<p>Существуют варианты «английской» и «французской» елочки. Первой появилась английская и она представляет собой уложенные простым зигзагом дощечки. Французский вариант предлагает более сложную фактуру за счет косого зареза планки, что сложней технологически, но имеет более выразительный, элегантный, цельный вариант покрытия.</p>
<p>Укладка паркета елочка применяется в направлении вдоль стен или по диагонали. В первом случае работа начинается от центра, обрезанные края остаются у стен. Во втором варианте в центровке нет необходимости.</p>
<p>Елочка может быть также двойная или тройная, при этом каждый элемент выкладывается не из одной дощечки, а из двух или трех. Этот способ визуально увеличивает ширину плашки при стабильной длине. Также елочка может быть из разноформатного паркета. Технологически это более сложные методы, т. к. необходимо подбирать по цвету и фактуре дощечки так, чтобы они сочетались, делать срезы определенной длины, для эффекта заданной асимметрии.</p>
<p>Приступая к работе, укладывают ориентир из шнура, вдоль которого и раскладывают рисунок. Сложность заключается в том, чтобы исключить возможность перекоса планок, особенно если работы ведутся клеевым способом. На следующем этапе укладывать продолжают в обе стороны от маячной елочки. Завершить композицию можно фризом.</p>
<h3><a name="razbejka"></a>Разбежка</h3>
<p><a href="/wp-content/uploads/2016/02/parket-razbejka.jpg" rel="attachment wp-att-5734"><img style="float: left; margin-right: 30px; margin-bottom: 20px;" src="/wp-content/uploads/2016/02/parket-razbejka-300x300.jpg" alt="parket-razbejka" width="300" height="300" class="alignleft size-medium wp-image-5734" srcset="https://parkettservice.ru/wp-content/uploads/2016/02/parket-razbejka-150x150.jpg 150w, https://parkettservice.ru/wp-content/uploads/2016/02/parket-razbejka-300x300.jpg 300w, https://parkettservice.ru/wp-content/uploads/2016/02/parket-razbejka.jpg 400w" sizes="(max-width: 300px) 100vw, 300px" /></a>
<p>Укладка паркета в разбежку получила широкое распространение в советские времена. Такой способ также называют «Первый номер» или «Палуба».</p>
<p>Этот метод придает напольному покрытию монолитную структуру за счет того, что плашки скреплены с соседними со всех сторон замками на всей площади паркета. В результате паркет практичен и надежен.</p>
<p>Направление укладки может быть:</p>
<ul>
<li>вдоль длинной стены;</li>
<li>диагональное или под другим заданным углом;</li>
<li>зависеть от падения света;</li>
<li>в направлении хода интенсивного движения.</li>
</ul>
<p>Название способа раскладки описывает рисунок, при котором каждая следующая плашка сдвигается по отношению к предыдущей на 1/2, 1/3 или игриво — хаотично, в результате чего получится ассиметричный узор. Разбежка, как и елочка, может быть двойной или тройной, изменяя плотность рисунка. Во время укладки ориентир размещают вдоль выбранного направления. В первый ряд дощечки укладываются целиком. Начиная со второго ряда первую плашку подрезают на заданную длину с целью получить желаемый рисунок разбежки. Это самый экономичный вариант паркета как по минимизации отходов материалов, так и по стоимости выполнения работы, так как является самым простым в реализации.</p>
<h3><a name="pletenka"></a>Плетенка</h3>
<p><a href="/wp-content/uploads/2016/02/parket-pletenka.jpg" rel="attachment wp-att-5735"><img style="float: left; margin-right: 30px; margin-bottom: 20px;" src="/wp-content/uploads/2016/02/parket-pletenka-300x211.jpg" alt="parket-pletenka" width="300" height="211" class="alignleft size-medium wp-image-5735" srcset="https://parkettservice.ru/wp-content/uploads/2016/02/parket-pletenka-300x211.jpg 300w, https://parkettservice.ru/wp-content/uploads/2016/02/parket-pletenka.jpg 585w" sizes="(max-width: 300px) 100vw, 300px" /></a>
<p>Укладка паркета плетенкой – самый распространенный способ художественного оформления паркета. Рисунок представляет собой переплетенные дощечки. Для достижения объемного эффекта используют не менее двух пород дерева. Выглядит красиво, стильно, обеспечивает возможность индивидуального декора.</p>
<p>В данном случае раскладки могут быть разные вариации с количеством планок в набираемом элементе – рисунок в одну, две, три и, даже четыре дощечки. В качестве акцента используют контрастные или сочетающиеся другим способом, вставки. Существуют варианты рисунков из разноформатных плашек, что придает уникальность мотиву.</p>
<p>Способ укладки плетенка кардинально отличается от классических, когда направление работы стартует от стены или угла, продолжая повторять заданный маячным рядом угол. Монтаж плетенки происходит послойно, от наружных элементов рисунка к внутренним. Укладка бывает в прямом направлении или по диагонали, чем достигается более выгодный ракурс, однако, увеличивается расход материалов.</p>
<p>Сложность работы заключается в высокой точности укладки. Это требует большой квалификации мастера и стоимость таких работ существенно превышает расценки предыдущих вариантов. Также необходимо отметить, что и уход за таким паркетом, сочетающим несколько пород древесины, требуется особый.</p>
<h3><a name="kvadrat"></a>Квадраты</h3>
<p><a href="http://test.parkett-service.ru/wp-content/uploads/2016/02/parket-kavdrat.jpg" rel="attachment wp-att-5737"><img style="float: left; margin-right: 30px; margin-bottom: 20px;" src="http://test.parkett-service.ru/wp-content/uploads/2016/02/parket-kavdrat-300x225.jpg" alt="parket-kavdrat" width="300" height="225" class="alignleft size-medium wp-image-5737" srcset="https://parkettservice.ru/wp-content/uploads/2016/02/parket-kavdrat-300x225.jpg 300w, https://parkettservice.ru/wp-content/uploads/2016/02/parket-kavdrat.jpg 800w" sizes="(max-width: 300px) 100vw, 300px" /></a>
<p>Укладка паркета квадратами бывает трех видов:</p>
<ul>
<li>прямой квадрат – «вьетнамка»;</li>
<li>развернутый квадрат;</li>
<li>сложный квадрат – «колодец».</li>
</ul>
<p>Использование различных пород дерева придает уникальность рисунку, оформляют красивый орнамент. Основой для этого метода являются блоки клепок, которые располагаются под углом в 90 градусов, образовывая квадраты с перпендикулярно выложенными дощечками, обеспечивающими игру света. Технология укладки прямого квадрата заключается в укладке планок вдоль стен комнаты. Для маяка в квадратном помещении выкладывают от стены, которая расположена напротив входа. В случае прямоугольной комнаты, работу начинают вдоль любой длинной стены.</p>
<p>При работе над развернутым квадратом паркетные элементы располагают под углом в 45 градусов к стенам, по диагонали комнаты. Недостатком такого метода является большое количество отходов, что ведет к увеличению бюджета. Для реализации квадрата необходимо подобрать дощечки, у которых длина кратна ширине. Это необходимое условие для этого метода. Кроме того, узор из квадратов должен полностью вписываться в размеры комнаты, а это тоже зависит от длины планок. С такими тонкостями справится только квалифицированный специалист. Еще одна тонкость, которую необходимо учитывать при использовании этого метода укладки паркета. Крупный квадрат визуально уменьшит маленькое помещение. На большой площади потеряются маленькие квадраты.</p>


<h2>Обработка паркетного массива после укладки</h2>
<p><img class="alignleft" id="pict" src="https://parkettservice.ru/wp-content/uploads/2016/02/parquet-ukladka-2.jpg" style="float: left; margin-right: 30px; margin-bottom: 20px;">Работы с паркетом не заканчиваются этапом укладки. Для придания эстетического вида, безопасности использования и длительного срока эксплуатации необходимо провести еще ряд работ. Они включают шлифовку (циклевку), грунтовку и лакировку полов. Стоимость услуг мастеров зависит от уровня сложности и сроков выполнения работ.</p>
<h3>Шлифовка</h3>
<p>После укладки паркета его необходимо отшлифовать для достижения идеального внешнего вида и оптимальных эксплуатационных качеств.</p>
<p><b>Циклевка производится шлифовальными машинами:</b></p>
<ul>
<li>барабанными;</li>
<li>плоскошлифовальными.</li>
</ul>
<p>Грубую работу проводят барабанными машинами, которые снимают перепады между уровнями плашек. Важен точный подбор абразивного материала, чтобы не повредить древесину. Следующим этапом с помощью плоскошлифовальных машин выполняют тонкую обработку паркета, достигая при этом идеальной гладкости поверхности покрытия. Целью этих работ является не только доведение поверхности до ровного состояния, но и устранение возможных загрязнений, появившихся в процессе выполнения предыдущих этапов укладки паркета. В целом процедуру циклевки необходимо повторить, как минимум три раза.</p>
<p><strong>Цены на шлифовку паркета</strong>
</p>
<ul class="priceList">
<li><span> Стандартная циклевка</span>: <b>150</b> p.</li>
<li><span> Дополнительная шлифовка</span>: <b>100</b> p.</li>
