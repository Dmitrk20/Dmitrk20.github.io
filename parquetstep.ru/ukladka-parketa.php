</ul>
<h3>Нанесение защитного покрытия</h3>
<p>Отшлифованная поверхность обрабатывается грунтовкой, которая пропитывает поры древесины, сокращая тем самым расход масла или лака при последующей обработке. Кроме того, грунтовка несет на себе функцию зашиты поверхности паркета от агрессивности лака и сохранения натурального цвета дерева. Для достижения общей концепции в оформлении интерьера помещения и получения необходимого тона паркета, применяют тонирующие грунтовки или специальные морилки.</p>
<p>Следующая обработка проводится маслом или лаком. Рассмотрим, в каких случаях оптимален тот или другой способ. Паркетное масло имеет ряд преимуществ: экологически чистый продукт, т. к. является натуральным; надежно защищает от влаги, при этом дерево дышит;достаточно просто в отношении реконструкционных работ; имеет высокую износостойкость. Однако есть и отрицательная характеристика. За паркетом под маслом необходим тщательный уход как минимум ежемесячное нанесение специальных средств. В противном случае паркет может потемнеть. Наносят масло в два слоя.</p>
<p>Лаку присущи свои плюсы: паркет под лаком прост в уходе; долговечность покрытия лаком не менее семи лет; при ремонте слой лака снимают полностью, значит, есть возможность выбрать другой оттенок. Минус тоже есть. При необходимости нанести лак только в месте, где он протерся, не получится. Придется шлифовать, а затем вскрывать лаком всю поверхность. Лаком обрабатывают в четыре слоя с промежуточной циклевкой между ними. Его можно использовать для тонирования паркета. Каким бы способом ни обрабатывали паркет, маслом или лаком, необходимо строго соблюдать все технологические требования.</p>
