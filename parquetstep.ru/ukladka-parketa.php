<h2>Способы укладки штучного и модульного паркета</h2>
<p>Существует 4 варианта, как настелить паркет. Рассмотрим каждый способ укладки паркета в деталях.</p>
<h3>Гвоздями к дощатому основанию</h3>
<p>В том случае, если основание выполнено из дерева, используют метод «на гвоздях» или «сухой». Применяют его, если уже имеется деревянное покрытие хорошего качества и проводить дополнительно работы по демонтажу и заливке стяжки считают нецелесообразным. Таким же способом проводится укладка паркета на фанеру, предварительно подготовленную для этих работ.</p>
<p>Преимущества:</p>
<ul>
<li>надежность конструкции;</li>
<li>долговечность;</li>
<li>возможность использования на больших площадях.</li>
</ul>
<p>Недостатки:</p>
<ul>
<li>высокая трудоемкость работ;</li>
<li>невозможность произвести ремонт в силу большого количества креплений.</li>
</ul>
<h3>Приклеивание с использованием специального клея</h3>
<p>При повышенном требовании к прочности и стойкости пола применяется укладка паркета на клей. Этот метод предполагает использовать специальный клей, который наносится непосредственно на подготовленную стяжку или влагостойкую фанеру зубчатым шпателем, обеспечивающим плотное соединение основания с паркетными дощечками. Обязательным условием является наличие технического шва шириной в 10 мм, который компенсирует сезонное расширение древесины, тем самым минимизируя деформацию паркета. Преимущества у этого способа такие, как у предыдущего.</p>
<p>Преимущества:</p>
<ul>
<li>надежность;</li>
<li>подходит для больших площадей полов.</li>
<li>при укладке на стяжку стоимость работ по укладке паркета снижается.</li>
</ul>
<p>Недостатки:</p>
<ul>
<li>клей может содержать агрессивные вещества, которые способны оказывать негативное воздействие на внешний вид древесины.</li>
</ul>
<h3>Монтаж на битумную мастику</h3>
<p>Монтаж паркета с применением битумных мастик – старый метод и используется в основном по цементной стяжке. В силу своих негативных качеств, такой способ сегодня практически не применяют.
</p>
<p>Преимущества:</p>
<ul>
<li>простой ремонт такого паркета.</li>
</ul>
<p>Недостатки:</p>
<ul>
<li>высокая степень текучести материала даже в холодном состоянии подвергает паркетные планки расползанию с образованием щелей;</li>
<li>со временем мастика теряет свою эластичность и прочность. Вследствие этого она начинает крошиться и не выполняет свои функции по фиксации паркетных плашек. Кроме того, появляется скрип от трения деревянных деталей между собой.</li>
</ul>
<h3>«Плавающий» способ</h3>
<p>Плавающая укладка паркета предполагает крепление планок методом шип-паз без крепления к основанию.</p>
<p>Преимущества:</p>
<ul>
<li>данный тип соединения не подвержен изменениям с течением времени;</li>
<li>способен выдерживать значительные колебания температуры в помещении;</li>
<li>снижается цена укладки штучного паркета за счет отсутствия соединяющих средств;</li>
<li>появляется возможность легко производить ремонт.</li>
</ul>
<p>Недостатки:</p>
<ul>
